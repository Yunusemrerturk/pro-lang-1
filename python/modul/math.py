math.sin(x)             # sinüs'ünü alır
math.sinh(x)            # hiperbolik sinüs'ünü alır
math.sqrt(x)            # kökünü alır
math.tan(x)             # tanjannt'ını alır
math.tanh(x)            # hiperbolik tanjantını alır
math.trunc(x)           # 8'lik tabanındaki değerini verir
math.pi                 # sabit pi sayısı
math.e                  # sabit e sayısı


